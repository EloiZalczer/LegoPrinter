CREATE TABLE PROJECT(
       project_id serial primary key,
       project_name varchar(255),
       last_modified timestamp,
       sizex integer,
       sizey integer,
       sizez integer
);

CREATE TABLE PIECES(
       type serial primary key,
       sizx integer,
       sizey integer
);

